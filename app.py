import streamlit as st
import google.generativeai as genai

# جلب المفتاح السري بأمان من إعدادات Streamlit المخفية
api_key = st.secrets["GEMINI_API_KEY"]
genai.configure(api_key=api_key)

# إعدادات واجهة الصفحة
st.set_page_config(page_title="محول المساحات العقارية | إتقان", page_icon="🏢")

# تصميم الواجهة الرئيسية
st.title("🏢 محول المساحات العقارية اليمني")
st.markdown("**إتقان للمقاولات والاستشارات الهندسية**")
st.info("أداة معتمدة لتحويل مساحات الأراضي بين الوحدات المحلية (القصبة، اللبنة، المعاد، الفدان) والمتر المربع.")

# مربع إدخال النص
user_input = st.text_input("اكتب المساحة والوحدة المراد تحويلها (مثال: كم تساوي 15 لبنة صنعاني بالقصبة الإبية؟):")

# زر التحويل
if st.button("تحويل المساحة"):
    if user_input:
        with st.spinner('جاري الحساب والتحويل...'):
            try:
                # دمج تعليمات النظام الثابتة مع طلب المستخدم
                prompt = f"""
                أنت خبير في المساحة والعقارات في اليمن. 
                قواعد التحويل المعتمدة (الذراع = 0.45 متر):
                - قصبة عشاري: 20.25 م²
                - قصبة اثنا عشري: 29.16 م²
                - قصبة أربعة عشري: 39.69 م²
                - قصبة ستة عشري: 51.84 م²
                - قصبة إبية: 56.25 م²
                - لبنة صنعاني (عشاري): 44.44 م²
                - لبنة عمران/محويت (اثنا عشري): 64 م²
                - لبنة ذمارية: 114.49 م²
                - معاد تهامي: 4355 م²
                - معاد زبيدي: 3600 م²
                - فدان: 4200 م²
                
                استخدم المتر المربع كقيمة مرجعية. أجب بنص مباشر، دقيق، واحترافي دون فلسفة زائدة.
                إذا كانت الوحدة غير معروفة، اعتذر بلباقة.
                
                طلب المستخدم: {user_input}
                """
                
                # استدعاء الذكاء الاصطناعي
                model = genai.GenerativeModel('gemini-1.5-flash')
                response = model.generate_content(prompt)
                
                # عرض النتيجة
                st.success("النتيجة النهائية:")
                st.write(response.text)
                
            except Exception as e:
                st.error("حدث خطأ في الاتصال، يرجى المحاولة مرة أخرى.")
    else:
        st.warning("يرجى كتابة المساحة في المربع أعلاه أولاً.")
